nome = str(input("Digite seu nome: "))

if nome == "Davi":
    print(f"Que nome bonito {nome}!")
elif nome == "Ana" or nome == "Pedro":
    print(f"Que nome legal {nome}!")
elif nome == "Lula" or nome == "Alexandre":
    print(f"Seu nome é horrivel {nome}!")
else:
    print(f"Olá tubo bem {nome}!")
